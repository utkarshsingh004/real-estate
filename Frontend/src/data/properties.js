// Function to get properties with filters
export const getProperties = (filters = {}) => {
  let filteredProperties = [...featuredProperties]
  
  // Apply location filter if provided
  if (filters.location) {
    filteredProperties = filteredProperties.filter(property => 
      property.location.toLowerCase().includes(filters.location.toLowerCase())
    )
  }
  
  // Apply property type filter if provided
  if (filters.type) {
    filteredProperties = filteredProperties.filter(property => 
      property.type === filters.type
    )
  }
  
  // Apply price range filter if provided
  if (filters.price) {
    const [min, max] = filters.price.split('-').map(Number)
    filteredProperties = filteredProperties.filter(property => 
      property.price >= min && property.price <= max
    )
  }
  
  // Apply bedrooms filter if provided
  if (filters.bedrooms) {
    filteredProperties = filteredProperties.filter(property => 
      property.bedrooms >= parseInt(filters.bedrooms)
    )
  }
  
  // Apply status filter if provided
  if (filters.status) {
    filteredProperties = filteredProperties.filter(property => 
      property.status === filters.status
    )
  }
  
  return filteredProperties
}

// Function to get a property by ID
export const getPropertyById = (id) => {
  return featuredProperties.find(property => property.id === parseInt(id))
}

// Function to get related properties
export const getRelatedProperties = (propertyId, limit = 3) => {
  const currentProperty = getPropertyById(propertyId)
  if (!currentProperty) return []
  
  return featuredProperties
    .filter(property => property.id !== propertyId && property.type === currentProperty.type)
    .sort(() => 0.5 - Math.random())
    .slice(0, limit)
}